#!/usr/bin/python

#Program for example for Nested List 

fruits = [ "Strawberry", "Nectrains", "Apples", "Grapes", "Peaches", "Cherries", "Pears" ]
vegetables = [ "Spinach", "Kale", "Tomatoes", "Celery", "Potatoes" ]
nested_list = [ fruits, vegetables ]

print("****************************************************************************************")
print("****************************************************************************************")
print(f"Fruits List Contain :: {fruits} ")
print(f"Vegetables List Contain :: {vegetables} ")
print(f"Nested List having both Fruits and Vegetables :: {nested_list}")

print(f"nested_list[0] :: {nested_list[0]}")
print(f"nested_list[1] :: {nested_list[1]}")

print(f"nested_list[0][0] :: {nested_list[0][0]}")
print(f"nested_list[0][1] :: {nested_list[0][1]}")

print(f"nested_list[1][0] :: {nested_list[1][0]}")
print(f"nested_list[1][1] :: {nested_list[1][1]}")
print("****************************************************************************************")
print("****************************************************************************************")
